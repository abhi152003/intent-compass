// Action enum mapping
export enum IntentAction {
  STAKE = 0,
  LEND = 1,
  SWAP = 2,
}

// Helper to get contract address for a chain
export function getIntentExecutorAddress(chainId: number): string | undefined {
  return INTENT_EXECUTOR_ADDRESSES[chainId];
}

// Check if contract is deployed on a chain
export function isContractDeployed(chainId: number): boolean {
  const address = INTENT_EXECUTOR_ADDRESSES[chainId];
  return address !== '0x0000000000000000000000000000000000000000' && address !== undefined;
}

